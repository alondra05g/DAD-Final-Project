#determine pareto optimality using pairwise swaps
def pareto_optimal(assignment, utilities, num_students):

    for i in range(num_students):
        for j in range(num_students):
            if i >= j:
                continue

            p_i = assignment[i]
            p_j = assignment[j]

            u_i_current = utilities[i, p_i]
            u_j_current = utilities[j, p_j]

            u_i_swap = utilities[i, p_j]
            u_j_swap = utilities[j, p_i]

            #pareto improvement condition
            if (u_i_swap >= u_i_current and
                u_j_swap >= u_j_current and
                (u_i_swap > u_i_current or u_j_swap > u_j_current)):
                
                return False, (i, j, p_i, p_j)

    return True, None


#check pareto optimality
is_PO, blocking = pareto_optimal(assignment, utilities, num_students)

